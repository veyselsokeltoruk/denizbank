<script setup>
import AOS from 'aos';
import 'aos/dist/aos.css';
import { onMounted, ref } from 'vue';

// Animation counters
const nakdiKrediBuyumesi = ref(0);
const takiptekiKrediler = ref(0);
const ozkaynakKarliligi = ref(0);

// Animasyon durumunu takip için
const isAnimating = ref(false);

onMounted(() => {
  // Initialize AOS animation library with interactivity settings
  AOS.init({
    duration: 800,
    once: false,
    mirror: true,
    offset: 50,
    easing: 'ease-in-out'
  });

  const animateCount = (targetRef, finalValue, duration) => {
    const steps = 60;
    const stepValue = finalValue / steps;
    let current = 0;
    const interval = duration / steps;

    const timer = setInterval(() => {
      current += stepValue;
      if (current >= finalValue) {
        targetRef.value = finalValue;
        clearInterval(timer);
      } else {
        targetRef.value = Math.round(current * 10) / 10;
      }
    }, interval);
  };

  // Start animations with slight delays
  setTimeout(() => animateCount(nakdiKrediBuyumesi, 51.1, 2000), 500);
  setTimeout(() => animateCount(takiptekiKrediler, 2.1, 2500), 700);
  setTimeout(() => animateCount(ozkaynakKarliligi, 33.3, 3000), 900);

  // Scroll eventi dinle ve AOS animasyonlarını yeniden başlat
  window.addEventListener('scroll', function() {
    if (!isAnimating.value) {
      isAnimating.value = true;
      setTimeout(() => {
        AOS.refresh();
        isAnimating.value = false;
      }, 200);
    }
  });
});
</script>

